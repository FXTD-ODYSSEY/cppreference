// cplusplus link https://cplusplus.com/reference/cmath\fabs
/* fabs example */
#include <math.h> /* fabs */
#include <stdio.h> /* printf */

int main()
{
    printf("The absolute value of 3.1416 is %f\n", fabs(3.1416));
    printf("The absolute value of -10.6 is %f\n", fabs(-10.6));
    return 0;
}
