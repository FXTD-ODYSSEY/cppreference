// cplusplus link https://cplusplus.com/reference/cmath\lround
/* lround example */
#include <math.h> /* lround */
#include <stdio.h> /* printf */

int main()
{
    printf("lround (2.3) = %ld\n", lround(2.3));
    printf("lround (3.8) = %ld\n", lround(3.8));
    printf("lround (-2.3) = %ld\n", lround(-2.3));
    printf("lround (-3.8) = %ld\n", lround(-3.8));
    return 0;
}
