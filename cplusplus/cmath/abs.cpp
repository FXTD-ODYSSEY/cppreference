// cplusplus link https://cplusplus.com/reference/cmath\abs
// cmath's abs example
#include <cmath> // std::abs
#include <iostream> // std::cout

int main()
{
    std::cout << "abs (3.1416) = " << std::abs(3.1416) << '\n';
    std::cout << "abs (-10.6)  = " << std::abs(-10.6) << '\n';
    return 0;
}
