// cplusplus link https://cplusplus.com/reference/cmath\llround
/* llround example */
#include <math.h> /* lround */
#include <stdio.h> /* printf */

int main()
{
    printf("llround (2.3) = %lld\n", llround(2.3));
    printf("llround (3.8) = %lld\n", llround(3.8));
    printf("llround (-2.3) = %lld\n", llround(-2.3));
    printf("llround (-3.8) = %lld\n", llround(-3.8));
    return 0;
}
