/* remainder example */
#include <math.h> /* remainder */
#include <stdio.h> /* printf */

int main()
{
    printf("remainder of 5.3 / 2 is %f\n", remainder(5.3, 2));
    printf("remainder of 18.5 / 4.2 is %f\n", remainder(18.5, 4.2));
    return 0;
}
