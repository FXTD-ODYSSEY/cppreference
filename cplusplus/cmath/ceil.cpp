/* ceil example */
#include <math.h> /* ceil */
#include <stdio.h> /* printf */

int main()
{
    printf("ceil of 2.3 is %.1f\n", ceil(2.3));
    printf("ceil of 3.8 is %.1f\n", ceil(3.8));
    printf("ceil of -2.3 is %.1f\n", ceil(-2.3));
    printf("ceil of -3.8 is %.1f\n", ceil(-3.8));
    return 0;
}
