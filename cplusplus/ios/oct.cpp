#include <iostream> // std::cout, std::dec, std::hex, std::oct

int main()
{
    int n = 70;
    std::cout << std::dec << n << '\n';
    std::cout << std::hex << n << '\n';
    std::cout << std::oct << n << '\n';
    return 0;
}
